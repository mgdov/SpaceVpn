export function getBlogPost(id: number): BlogPost | undefined {
  return blogPosts.find((post) => post.id === id)
}

export function getRelatedPosts(currentId: number, limit = 2): BlogPost[] {
  return blogPosts.filter((post) => post.id !== currentId).slice(0, limit)
}
